#include <iostream>
using namespace std;

int main() {

    //specifying the array size
    int arr[4]={1,2,3,4};
    cout<<arr[0]<<endl;
    cout<<arr[1]<<endl;
    cout<<arr[2]<<endl;
    cout<<arr[3]<<endl;

    //not specifying the array size
    int arr1[]={1,2,3,4};
    cout<<arr1[0]<<endl;
    cout<<arr1[1]<<endl;
    cout<<arr1[2]<<endl;
    cout<<arr1[3]<<endl;

    return 0;
}